import math
a = -1
b = -2
c = 3
print((-b + math.sqrt(b ** 2 - 4 * a * c))/ (2 * a))
print((-b - math.sqrt(b ** 2 - 4 * a * c))/ (2 * a))
# 乘方：**
# 运算顺序：括号（）；平方**； 乘除* /；加减=-
# 2的三次方：2 ** 3
# print为内置函数，math是一个专门提供数学运算函数的库，导入方法是在文件最开始一行输入import math
